import { Component, OnInit } from '@angular/core';
import { RequirmentService } from '../requirment.service';
import { Router } from '@angular/router';

@Component({
  selector: 'app-update',
  templateUrl: './update.component.html',
  styleUrls: ['./update.component.css']
})
export class UpdateComponent implements OnInit {

  constructor(private RequirmentService :RequirmentService, private router:Router) { }

  ngOnInit() {
  }

  addreq(reqform){
    this.RequirmentService.updateRequirment(reqform.value).subscribe(res => {
      console.log('requirment updated successfully');
      alert('requirment updated Successfully...!');
      this.router.navigateByUrl("/update");
      reqform.reset();
   }, err => {
      console.log('Requirment is  not updated');
   });
  }
}

























































































































































































































































































